/**
 *  Simple class for instantiation of objects to act as simple
 *  replacements for tuples.
 */
public class Pair<X, Y> {
	
	public final X valueOne;
	public final Y valueTwo;
	
	public Pair(X valueOne, Y valueTwo) {
		this.valueOne = valueOne;
		this.valueTwo = valueTwo;
	}
	
}
